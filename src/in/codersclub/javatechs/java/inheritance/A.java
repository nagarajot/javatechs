public class A
{
	private int a;
	protected int b;
	public int c;
	
	
	private void m1()
	{
		System.out.println("Inside private m1()");
	}
	
	protected void m2()
	{
		System.out.println("Inside protected m2()");
	}
	
	public void m3()
	{
		System.out.println("Inside public m3()");
	}
}


